package NivelFacil.TiposDeDados;

public class DadosNaoPrimitivos {
    public static void main(String[] args) {

        String nome = "Naruto Uzumaki";
        String nomeUpperCase = nome.toUpperCase();

        System.out.println("nomeUpperCase = " + nomeUpperCase);
        System.out.println("nome " + nome);

        String aldeia = "Aldeia da Folha";
        String aldeiaLowerCase = aldeia.toLowerCase();

        System.out.println("aldeiaLowerCase = " + aldeiaLowerCase);
        System.out.println("aldeia = " + aldeia);
    }
}
